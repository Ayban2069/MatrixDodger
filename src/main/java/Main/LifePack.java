package Main;

public class LifePack {
    private int x, y;
    private int size;

    public LifePack(int x, int y, int size) {
        this.x = x;
        this.y = y;
        this.size = size;
    }

    public int getX() { return x; }
    public int getY() { return y; }
    public int getSize() { return size; }
}
